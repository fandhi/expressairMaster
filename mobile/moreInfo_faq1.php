            <ul>
                <li class="question">How do I book a Garuda Indonesia flight ticket? </li>
                <li class="answer">Just visit the nearest Garuda Indonesia office or call the Garuda Indonesia Call Center at Tel. 0 804 1 807 807 or 021 2351 9999, or simply contact your nearest Garuda Indonesia travel agent.</li>
            
                <li class="question">How do I modify my itinerary during the booking process?</li>
                <li class="answer">You can modify your itinerary as much as you want so long as you use the same Booking Reference (Booking Code) and don’t create a new Booking Reference.</li>
            
                <li class="question">How long can I hold my reservation until I purchase the ticket?</li>
                <li class="answer">You can hold a reservation as long as the ticketing deadline is still valid.</li>
            
                <li class="question">How many passengers can I book in one reservation?</li>
                <li class="answer">You can book as many as 9 passengers for Individual booking and 99 passengers for Group Booking, including children or infants occupying a seat.</li>
            
                <li class="question">Why must a local contact be included in a reservation?</li>
                <li class="answer">A local contact is needed by Garuda Indonesia to inform a passenger of necessary information concerning his/her flight.</li>
            
                <li class="question">How can I check my existing reservation?</li>
                <li class="answer">You can view it at www.garuda-indonesia.com. Find “Booking Status” and then type your six numeric Passenger Name Record that starts with Q. Then go to the “Check Status” column. </li>
            
                <li class="question">How do I reconfirm my reservation?</li>
                <li class="answer">Garuda Indonesia does not require reconfirmation any longer, but you can provide us with your local contact number at your destination or your mobile phone number to reconfirm your onward journey.</li>
            
