            <h1>FAQ - Fare Ticketing</h1>
            <ul>
                <li class="question">What is a CHILD or INFANT FARE?</li>
                <li class="answer">Child fare means a fare for a person who has reached his/her 2nd but not yet his/her 12th birthday at the start of travel. 
                    Infant fare means the fare for a person who has not reached his/her 2nd birthday.</li>
            
                <li class="question">How can I get more information about the range of prices Garuda Indonesia offers?</li>
                <li class="answer">Call Garuda Indonesia Call Center 0804 1 807 807 or (021) 2351 9999, any Garuda Indonesia sales office, or your travel agent.</li>
            
                <li class="question">Which destinations have a range of prices?</li>
                <li class="answer">All GA destinations, domestic and international, have a range of prices.</li>
            
                <li class="question">Why do ticket prices change from time to time?</li>
                <li class="answer">To accommodate market dynamics / customer needs.</li>
            
                <li class="question">What is ticket flexibility?</li>
                <li class="answer">Ticket rules/conditions that are attached to tickets are specified according to fares chosen by the customer.</li>
            
                <li class="question">Why should there be a range of prices for the same destination?</li>
                <li class="answer">To provide specific services to customers of various segments.</li>
            
                <li class="question">What is the difference between Economy and Executive Class?</li>
                <li class="answer">Economy class offers seating standards equal to “Tourist Class" and less than standards provided for Executive Class. Executive Class offers superior seating standards than those provided on economy / tourist class.</li>
            
                <li class="question">I have a refundable Garuda Indonesia ticket that I am not going to be able to use. How can I obtain a refund?</li>
                <li class="answer">Unless specified in the fare conditions, an International / Domestic ticket can be refunded directly at any Garuda Indonesia sales office or travel agent’s office, which originally collected ticket payment. 
                    For further information, please call the Garuda Indonesia Call Center at Tel. 0804 1 807 807, any Garuda Indonesia sales office or travel agent.</li>
            
                <li class="question">If I return my ticket, how long does it take to receive a refund?</li>
                <li class="answer">If a ticket is issued and refunded at the same Garuda Indonesia ticketing office, the refund will take approximately 2 weeks. The process may take longer if the ticket was issued by a travel agent.</li>
                
                <li class="question">I have a non-refundable Garuda Indonesia ticket that I am not going to use. Can I still get a refund?</li>
                <li class="answer">We are sorry to say that this is not possible as it is against regulations.</li>
                
                <li class="question">Can I change my ticket or get a refund if my plans were changed due to unforeseen circumstances?</li>
                <li class="answer">Yes. Your request will be considered and may be judged as an Involuntary Refund.</li>
                
                <li class="question">I purchased a prepaid ticket for my friend and he did not travel. How do I get my refund?</li>
                <li class="answer">A ticket issued against a Prepaid Ticket Advice (PTA) will be refunded only to the sponsor of the PTA. The passenger may apply for refund in accordance with GA policy.</li>
                
                <li class="question">Is it possible to up-grade my class to a higher one?</li>
                <li class="answer">Yes, it is possible. Go to the nearest Garuda Indonesia sales office to recalculate ticket payment.</li>
                
                <li class="question">What is the easiest way to purchase a ticket?</li>
                <li class="answer">There are 5 different methods using our Garuda Indonesia on-line payment system: ATM, Phone Banking, Internet Banking, Mobile Banking, or a Visa or MasterCard credit card. 
                    For more information on how to make a reservation and purchase a ticket, click here.</li>
                
                <li class="question">How can an airline ticket be purchased without an ATM card or credit card?</li>
                <li class="answer">Cash payments are accepted at any Garuda Indonesia branch office or travel agent. Click for list of Garuda Indonesia branch offices.</li>
                
                <li class="question">I lost my ticket. What should I do?</li>
                <li class="answer">If you lost an electronic ticket document, you can ask any Garuda Indonesia branch office or travel agent issuing office to re-print the ticket if you have proper proof of identity. 
                    For further information call Garuda Indonesia <span style="color:#333">Call Center at Tel 0804 1 807 807</span>, or visit the nearest Garuda Indonesia sales office or travel agent.</li>
                
                <li class="question">I need a Garuda Indonesia ticket cover for a formal report to my office. How can I get a ticket cover?</li>
                <li class="answer">Garuda Indonesia passengers can keep the ticket cover for personal use or copy their flight coupon before flying.</li>
